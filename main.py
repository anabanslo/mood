mood = input("What is your mood today?")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Think of something positive.")
elif mood == "excited":
    print("Super!")
elif mood == "relaxed":
    print("It's nice to read that.")
else:
    print("I don't recognize this mood.")